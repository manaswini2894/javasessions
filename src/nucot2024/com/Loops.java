package nucot2024.com;

public class Loops {

	public static void main(String[] args) {
		 //for loop
        /*
         * variable_updation-->++ or --
         * 
         * for(variable_init;variable_condition;variable_updation)
         * {
         * 
         *             body/statements
         * 
         * }
         * 
         * 
         */
        
        //1 to 10
        
        
        
        
        System.out.println("Numbers from 0 to 10");
        
        for(int i=0;i<=10;i++)
        {
            
            
            System.out.println(i);
            
            
        }
        
        
        System.out.println("Numbers from 10 to 0");
        
        for(int ii=10;ii>=0;ii--)
        {
            
            
            System.out.println(ii);
            
            
        }
        
        System.out.println("Even Numbers from 0 to 10");
        
        for(int iii=0;iii<=10;iii=iii+2)
        {
            
            
            System.out.println(iii);
            
            
        }
        
        for(char c='a';c<='z';c++)
        {
        	System.out.println("characters from c--->"+(byte)c);
        }
        
        for(double d=0.0;d<=10.0;d++)
        {
        	System.out.println("+d");
        }
    }
}


	


